package Chapter03;

public class Operator10 {

	public static void main(String[] args) {
		
		String str1 = "JAVA";
		String str2 = "programing";
		
		// str1 str2 = 참조변수이다 => 객체를 가르키는 메모리 주소값을 저장하고있다.
		// 참조변수의 관계연산은 
		
		System.out.println(str1 == str2); //f
		System.out.println(str1 != str2); //t
		
		
	}

}
