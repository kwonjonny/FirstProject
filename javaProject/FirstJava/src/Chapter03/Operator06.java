package Chapter03;

public class Operator06 {

	public static void main(String[] args) {
		
		int num1 = 30;
		
		int num2 = 2;
		
		float num3 = 2.0f;
		
		System.out.println(num1 + num2); //32
		
		System.out.println(num1 - num2); //28
		
		System.out.println(num1 * num2); //60
		
		System.out.println(num1 / num2); //15
		
		System.out.println(num1 / 7);	 //4
		
		System.out.println(num1 / num3); //4.285714
		
		System.out.println(num1 % num2); //0
		
		System.out.println(num1 % 7); //2
		
		
	}

}
