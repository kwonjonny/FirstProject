package Chapter03;

public class Operator03 {

	public static void main(String[] args) {
		
		int num1=5;
		System.out.println("num1:"+ num1); //5
		
		int num2 = ++num1;
		System.out.println("num2:"+ num2); //6
		System.out.println("num1:"+ num1); //6
		
		System.out.println("++num1:"+ ++num1); //7
		
		
	}

}
