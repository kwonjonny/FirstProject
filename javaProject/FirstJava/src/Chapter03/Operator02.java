package Chapter03;

public class Operator02 {

	public static void main(String[] args) {
		boolean led = true;
		
		System.out.println(led); // T
		System.out.println(!led); // F
		System.out.println(led); // T

		int num1=5;
		int num2 =13;
		
		System.out.println("num1 > num2 =>" + (num1 > num2));
		
	}

}
