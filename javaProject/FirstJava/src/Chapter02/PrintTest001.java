package Chapter02;

public class PrintTest001 {
	
public static void main (String [] args) {
	
	// 출력 메소드 : print(); -> 매개변수에는 다양한 데이터를 전달받고 출력한다
	System.out.print("안녕하세요.");	
	System.out.print("반갑습니다.");			// 안녕하세요.반갑습니다. 의 출력
	
	System.out.println();					// 줄 바꿈
	
	System.out.println("저는 호날두 입니다.");	// 저는 호날두 입니다.
	System.out.println("축구선수입니다."); 		// 축구선수입니다.
	
	
	

}
}
