package Chapter02;

public class CastingOperator011 {

	public static void main(String[] args) {
		
		char c = 'a';
		System.out.println(c); //	char type int로의 형 변환 
		
		int n1 = 1;
		System.out.println(c+n1);
		
		int n2 = 10;
		System.out.println(n2); //	10
		
		float n3 = 10.0f;
		System.out.println(n2+n3); //	20.0
		
	}

}
